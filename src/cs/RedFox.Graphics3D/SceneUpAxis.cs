namespace RedFox.Graphics3D;

/// <summary>
/// Identifies which axis should be treated as up for a scene.
/// </summary>
public enum SceneUpAxis
{
    /// <summary>
    /// Uses the Y axis as up.
    /// </summary>
    Y = 0,

    /// <summary>
    /// Uses the X axis as up.
    /// </summary>
    X = 1,

    /// <summary>
    /// Uses the Z axis as up.
    /// </summary>
    Z = 2,
}