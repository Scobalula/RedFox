namespace RedFox.Graphics3D.OpenGL.Viewing;

/// <summary>
/// Specifies the axis treated as the up direction in scene coordinates.
/// </summary>
public enum SceneUpAxis
{
    /// <summary>The Y-axis is up.</summary>
    Y,

    /// <summary>The Z-axis is up.</summary>
    Z,

    /// <summary>The X-axis is up.</summary>
    X,
}
